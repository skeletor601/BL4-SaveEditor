export default function BottomTabBar() {
  const location = useLocation();
  const navigate = useNavigate();

  const activeKey = (() => {
    const p = location.pathname;
    if (p.startsWith("/mobile/search")) return "search";
    if (p.startsWith("/mobile/community")) return "community";
    if (p.startsWith("/mobile/translate")) return "translate";
    if (p.startsWith("/mobile/settings")) return "settings";
    return "build";
  })();

  return (
    <nav className="mobile-tabs">
      {TABS.map((tab) => (
        <button
          key={tab.key}
          className={`mobile-tab ${activeKey === tab.key ? "active" : ""}`}
          onClick={() => navigate(tab.path)}
          type="button"
        >
          {tab.icon}
          <span>{tab.label}</span>
        </button>
      ))}
    </nav>
  );
}
